fun main()
{
    print("Введите номер дня недели (1-7): ")
    val a = readLine()!!.toInt()

    val day = when (a)
    {
        1 -> "Понедельник"
        2 -> "Вторник"
        3 -> "Среда"
        4 -> "Четверг"
        5 -> "Пятница"
        6 -> "Суббота"
        7 -> "Воскресенье"
        else -> "Некорректный номер дня недели"
    }

    println("День недели: $day")
}